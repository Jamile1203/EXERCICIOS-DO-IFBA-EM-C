// Dado o tamanho da base e da altura de um retângulo, calcular a sua área e o seu perímetro

#include<stdio.h>

void main(void){
int	b,h;
int area,perimetro;

printf("Digite o valor da base do retangulo: ");
scanf("%d", &b);
printf("Digite o valor da altura do retangulo: ");
scanf("%d", &h);

    area = b*h;
    perimetro = (b+h+area+perimetro);
    
    printf("\n A area do retangulo e seu perimetro respectivamete eh %d, %d: ", area,perimetro);
   
    
	
}
