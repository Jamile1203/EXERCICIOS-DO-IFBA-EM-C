// Em uma cidade se deseja sincronizar os semáforos. Com isto, quando um semáforo abre (fica verde), os veículos que nele estavam parados tendem a encontrar os próximos 
//semáforos também abertos.Para que isto seja feito, os próximos semáforos precisam abrir um pouco depois, dependendo da velocidade permitida na via e da distância entre eles
//Assim, ao abrir o semáforo, um veículo começa a acelerar até atingir velocidade permitida, que mantém até chegar ao próximo semáforo, levando um certo tempo para percorrer essa distância.
// Para que encontre o próximo semáforo aberto, este deve abrir um pouco antes da chegada do veículo (por ex: 3 segundos antes).   
//  Faça assim um algoritmo que informe quanto tempo depois um semáforo deve abrir, dada as seguintes informações: 
// a. a distância desde o semáforo anterior b. a velocidade permitida da via   c. a aceleração típica dos carros

#include<stdio.h>

void main(void){

	
    float distancia, velocidade, aceleracao;
    printf("Digite a distancia percorrida: ");
    scanf("%f", &distancia);
    printf("\n Digite a velocidade percorrida: ");
    scanf("%f",&velocidade);
    printf("\n Digite a aceleração: ");
    scanf("%f",&aceleracao);

    float tempo = velocidade/aceleracao;

    tempo += distancia/velocidade;

    printf("%.0f\n", tempo);

       
}

